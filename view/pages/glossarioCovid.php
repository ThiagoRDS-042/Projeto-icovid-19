            <h1>Glossário Covid</h1>

            <p><strong>“Achatar a curva”:</strong> tentativa de diminuir a propagação do novo Coronavírus e impedir um aumento significativo no número de pessoas infectadas em um curto período de tempo. Ao praticar o isolamento social, os indivíduos saudáveis podem ajudar a retardar a proliferação da doença, ajudando a “achatar a curva” dos gráficos de novos casos de Covid-19.</p>

            <p><strong>Assintomático:</strong> aquela pessoa que não apresenta sinais ou sintomas de doença pelo novo Coronavírus. Não se sabe se estas pessoas podem, em algum momento, serem transmissoras do vírus. Os sintomas mais frequentes da Covid-19 são: febre (a partir de 37,8ºC), tosse seca e, em casos mais graves, falta de ar.</p>

            <p><strong>Autoisolamento:</strong> a prática de alguém doente se separar espontaneamente daqueles que estão saudáveis, a fim de prevenir a disseminação da doença. Algumas estratégias incluem limitar-se a um único quarto/banheiro durante o período de recuperação e não sair em público até que o perigo de transmissão tenha passado.</p>

            <p><strong>Caso suspeito:</strong> paciente que apresenta sinais ou sintomas que podem ser sugestivos de infecção pelo novo Coronavírus.</p>

            <p><strong>Coronavírus:</strong> um grupo de vírus capaz de causar doenças em humanos e animais. O novo Coronavírus, conhecido como SARS-CoV2, causa a doença Covid-19. São da mesma família o SARS-CoV e o MERS-CoV, além de outros coronavírus que causam normalmente resfriados comuns. Sua estrutura é formada por micro espinhos quando vista ao microscópio eletrônico, que se parecem muito com uma coroa. É daí que vem o nome de “corona”.</p>

            <p><strong>Covid-19:</strong> é o nome da doença causada pelo vírus SARS-CoV-2 e é uma abreviação de Corona VIrus Disease (“doença causada pelo vírus Corona”, em tradução literal do inglês).</p>

            <p><strong>Distância social:</strong> recomendações a fim de reduzir a propagação da doença, como manter pelo menos um metro de distância das pessoas, evitar aglomerações e cancelar viagens desnecessárias a fim de reduzir a propagação da doença.</p>

            <p><strong>EPI:</strong> equipamento de proteção individual que é usado para diminuir a exposição a riscos que podem causar doenças ou ferimentos.</p>

            <p><strong>Epidemia:</strong> aparecimento de um grande número de casos de determinada doença, em determinado período de tempo, em uma localização geográfica específica.</p>

            <p><strong>Estado de calamidade:</strong> situação anormal que pode ser decretado nas esferas municipais, estaduais e federal, desencadeado por algum desastre que afeta uma região comprometendo seu poder de resposta. Neste estado, o governo pode fazer compras emergenciais sem a realização de licitações para custear ações rápidas de combate ao problema e pode aumentar os gastos com contratação de força de trabalho.</p>

            <p><strong>Grupo de risco:</strong> pessoas com maior chance de terem quadros graves, caso adquiram a doença. São aqueles portadores de doenças crônicas (como problemas cardíacos, diabetes, insuficiência renal, doenças pulmonares e pacientes imunossuprimidos, como os oncológicos).</p>

            <p><strong>Paciente zero:</strong> primeiro humano acometido por alguma doença viral ou bacteriana.</p>

            <p><strong>Pandemia:</strong> disseminação mundial de uma nova doença contagiosa, que se espalha por diversos continentes e tem transmissão sustentada entre pessoas. A Organização Mundial da Saúde (OMS) declarou pandemia da Covid-19 em 11 de março de 2020.</p>

            <p><strong>Período de incubação:</strong> o tempo necessário para uma pessoa começar a mostrar sintomas da doença após ser exposta ao novo Coronavírus. No caso da Covid-19, acredita-se que esse tempo pode demorar, em média, entre 1a 14 dias para aparecer, de acordo com a OMS.</p>

            <p><strong>Quarentena:</strong> a prática de isolar pessoas que parecem saudáveis, mas que podem ter sido expostas a uma doença, como a Covid-19. As quarentenas podem ser autoimpostas ou impostas pelo governo.</p>

            <p><strong>SARS-CoV-2:</strong> nome oficial do vírus que causa a Covid-19 (o novo Coronavírus, chamado inicialmente de n-Cov).</p>

            <p><strong>Surto:</strong> casos repentinos de uma determinada doença, em uma área geográfica relativamente controlada, como foi o caso da Covid-19 no começo de janeiro de 2020, quando afetava somente a cidade de Wuhan, na China.</p>

            <p><strong>Taxa de transmissão (R0):</strong> taxa básica de capacidade de disseminação entre pessoas de um vírus. No caso do SARS-CoV-2, o número é de 2 a 3, ou seja, um portador da doença pode transmitir para mais 2 a 3 pessoas.</p>

            <p><strong>Teste RT-PCR:</strong> tipo de teste molecular realizado a partir de uma amostra biológica, em laboratório, que pode detectar presença de material genético do SARS-CoV-2. É o teste atualmente utilizado para confirmação laboratorial da Covid-19.</p>

            <p><strong>Transmissão comunitária ou sustentável:</strong> momento em que há ampla circulação do vírus na comunidade, ou seja, que já não é mais possível saber quem foi a fonte da contaminação.</p>

            <p><strong>Triagem:</strong> perguntas simples realizadas para determinar se alguém tem ou não o risco de ser portador de alguma doença. No caso do novo Coronavírus, a triagem pode ser: aferição de temperatura e perguntas sobre uma possível exposição a alguém que pode ser suspeito de ter Covid-19.</p>

            <p><strong>Vírus:</strong> agentes infecciosos muito pequenos (20 a 300 nanômetros) capazes de entrar em células vivas, se multiplicar e infectar seres vivos. Existem mais de 200 mil tipos de vírus no mundo, que representam e representam a maior diversidade biológica da natureza.</p>

